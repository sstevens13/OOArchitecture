package broker;

public class ServantSumIntegers {
	
	/*
	 *  adds two integers together, chose to return a wraparound value
	 *  rather than null if the Integer.MIN_VALUE Integer.MAX_VALUE or less 
	 *  than Integer.MIN_VALUE
	 */
	public int addIntegers(int val1, int val2) {
		return val1 + val2;
	}
	
}
